class OtherMedication {
  int id;
  String name;
  bool isNotify;
  String description;
  String imageUrl;

  OtherMedication({
    int id,
    String name,
    bool isNotify,
    String description,
    String imageUrl,
  }) {
    this.id = id;
    this.name = name;
    this.isNotify = isNotify;
    this.description = description;
    this.imageUrl = imageUrl;
  }
}
