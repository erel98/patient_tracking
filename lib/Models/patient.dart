class Patient {
  int id;
  String fullName;
  int age;
  double weight;
  String mobileNumber;

  Patient([
    int id,
    int age,
    double weight,
    String mobileNumber,
  ]) {
    this.id = id;
    this.fullName = fullName;
    this.age = age;
    this.weight = weight;
    this.mobileNumber = mobileNumber;
  }
}
